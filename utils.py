"""Simple Calculator"""


def add(a: int, b: int) -> int:
    """Adding two intigers"""
    return a + b


def subtract(a: int, b: int) -> int:
    """Subtracting two intigers"""
    return a - b


def multiply(a: int, b: int) -> int:
    """Multiplying two intigers"""
    return a * b


def divide(a: int, b: int) -> int:
    """Dividing two intigers"""
    return a / b
